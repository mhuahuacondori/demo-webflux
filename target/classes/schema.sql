CREATE SEQUENCE IF NOT EXISTS table_empleados_id_seq;
CREATE SEQUENCE IF NOT EXISTS table_proveedores_id_seq;

CREATE TABLE IF NOT EXISTS  EMPLEADOS(
ID integer NOT NULL DEFAULT nextval('table_empleados_id_seq'),
NOMBRE varchar(30) NULL,
APELLIDO varchar(30) NULL,
FECHA_NAC date NULL,
REPORTA_A int NULL,
EXTENSION int NULL,
CONSTRAINT PK_EMPLEADOS PRIMARY KEY (ID));

CREATE TABLE IF NOT EXISTS PROVEEDORES(
ID integer NOT NULL DEFAULT nextval('table_proveedores_id_seq'),
NOMBREPROV varchar(50) NOT NULL,
CONTACTO varchar(50) NOT NULL,
CELUPROV varchar(12) NULL,
FIJOPROV varchar(12) NULL,
CONSTRAINT PK_PROVEEDORES PRIMARY KEY (ID));